package castingTest;

public class CastingTest3 {
	public static void main(String[] args) {
		
		int a = 0;
		char dat1 = ' ';
		String str1 = "";
	
		
	// 문자열의 강제 형변환	
	  System.out.println((Integer.parseInt("10")) - 5); // 정수 5로 바뀐다
	  System.out.println((Float.parseFloat("10")));  // 10.0F 의 실수로 바뀐다	
	  System.out.println((Double.parseDouble("10"))); // 10.0의로 바뀐다
	  System.out.println((Boolean.parseBoolean("10"))); // false
	 
	  // char값은 문자열을 강제형변환 실킬수 없다.
		
	}
}
