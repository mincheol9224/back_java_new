package castingTest;

public class CastingTest2 {
     public static void main(String[] args) {
//      문자열
//		강제 형변환
//      문자열 + 정수 = 문자열
//		문자열 + 실수 = 문자열
//     연결
      System.out.println("1" + 3);// 13
      System.out.println("1" + (3 +8)); // 111
      System.out.println("1" + 10.5); // 110.5
      System.out.println("1" + 'A'); // 1A
      System.out.println("1" + true); // 1true
	}
}
